function oneManCompanyBlocks(params: {
  intro: Block[];
  orchestratorTitle: string;
  orchestratorBody: string;
  sources: { title: string; body: string; tag: string }[];
  engineBody: string;
  outputs: { title: string; body: string }[];
  bookLaunchTitle: string;
  bookLaunchBody: string;
  launchTracks: { title: string; body: string }[];
  siteName: string;
  siteBody: string;
  results: string[];
  rhythmHeading: string;
  rhythmSteps: { title: string; body: string }[];
  costTitle: string;
  costBody: string;
  bottomLineHeading: string;
  bottomLineBody: string;
  footerLine: string;
}): Block[] {
  return [
    ...params.intro,
    { tag: 'emphasis-box', text: `<strong>${params.orchestratorTitle}.</strong> ${params.orchestratorBody}` },
    { tag: 'h2', text: 'How the System Works, Start to Finish' },
    { tag: 'h3', text: 'The Two Sources' },
    ...params.sources.flatMap<Block>((s) => [
      { tag: 'h4', text: `${s.title} (${s.tag})` },
      { tag: 'p', text: s.body },
    ]),
    { tag: 'callout', label: 'Both feed into', text: `<strong>The AI Engine.</strong> ${params.engineBody}` },
    { tag: 'h3', text: 'Automatic Content Outputs' },
    ...params.outputs.map<Block>((o) => ({ tag: 'li', text: `<strong>${o.title}.</strong> ${o.body}` })),
    { tag: 'h3', text: params.bookLaunchTitle },
    { tag: 'p', text: params.bookLaunchBody },
    ...params.launchTracks.map<Block>((t) => ({ tag: 'li', text: `<strong>${t.title}.</strong> ${t.body}` })),
    { tag: 'callout', label: 'Everything publishes to', text: `<strong>${params.siteName}.</strong> ${params.siteBody}` },
    { tag: 'h3', text: 'Which Builds' },
    ...params.results.map<Block>((r) => ({ tag: 'li', text: r })),
    { tag: 'h2', text: 'What the Monthly Rhythm Looks Like' },
    { tag: 'pull-quote', text: params.rhythmHeading },
    ...params.rhythmSteps.map<Block>((step, i) => ({
      tag: 'step-card',
      eyebrow: String(i + 1),
      title: step.title,
      body: [{ tag: 'p', text: step.body }],
    })),
    { tag: 'stat-callout', stats: [{ value: '80%', label: params.costTitle, color: 'magenta' }] },
    { tag: 'p', text: params.costBody },
    { tag: 'conclusion-box', title: params.bottomLineHeading, paragraphs: [params.bottomLineBody] },
    { tag: 'p', text: `<em>${params.footerLine}</em>` },
  ];
}

