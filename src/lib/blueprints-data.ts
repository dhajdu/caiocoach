export function getBlueprintBySlug(slug: string): Blueprint | undefined {
  return BLUEPRINTS.find((b) => b.slug === slug);
}

export function getAllSlugs(): string[] {
  return BLUEPRINTS.map((b) => b.slug);
}

export function sortByDate(items: Blueprint[], direction: 'asc' | 'desc' = 'desc'): Blueprint[] {
  return [...items].sort((a, b) => {
    const cmp = a.date.localeCompare(b.date);
    return direction === 'desc' ? -cmp : cmp;
  });
}
